
export default class Account {

	owner;
	balance;

	constructor(owner, balance) {
		this.owner = owner;
		this.balance = balance;
	}

	getOwner() {
		return owner
	}

	getBalance() {
		return balance
	}

	setOwner(owner) {
		this.owner = owner
	}

	setBalance(balance) {
		this.balance = balance
	}

	
}